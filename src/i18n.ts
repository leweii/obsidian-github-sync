export type Lang = "en" | "zh";

let _lang: Lang = "en";

export function setLang(l: Lang): void { _lang = l; }
export function getLang(): Lang { return _lang; }

/** Return the current translation table. */
export const L = () => _lang === "zh" ? zh : en;

/** Replace {0}, {1}, … placeholders in a translated string. */
export function tf(str: string, ...args: (string | number)[]): string {
  return str.replace(/\{(\d+)\}/g, (_, i) => String(args[+i] ?? ""));
}

